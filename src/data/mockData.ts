const generateTempCurve = (targetTemp: number, holdTime: number, heatingRate: number): { time: number; value: number }[] => {
  const points: { time: number; value: number }[] = [];
  const heatTime = targetTemp / heatingRate;
  for (let t = 0; t <= heatTime; t += 5) {
    points.push({ time: t, value: Math.min(t * heatingRate, targetTemp) });
  }
  for (let t = heatTime; t <= heatTime + holdTime; t += 5) {
    points.push({ time: t, value: targetTemp + (Math.random() - 0.5) * 4 });
  }
  for (let t = heatTime + holdTime; t <= heatTime + holdTime + 60; t += 5) {
    const coolDown = t - heatTime - holdTime;
    points.push({ time: t, value: Math.max(targetTemp - coolDown * 1.5, 25) });
  }
  return points;
};

