const generateStressStrainCurve = (maxStress: number, modulus: number): { time: number; value: number }[] => {
  const points: { time: number; value: number }[] = [];
  const maxStrain = maxStress / modulus * 100;
  for (let s = 0; s <= maxStrain; s += 0.05) {
    const stress = s < maxStrain * 0.7 ? s * modulus : s * modulus * (1 - (s - maxStrain * 0.7) / maxStrain * 0.3);
    points.push({ time: s, value: Math.min(stress, maxStress) });
  }
  return points;
};

